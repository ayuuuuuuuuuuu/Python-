# 程序运行时能够存储计算结果或能表示值的抽象概念
# 在程序运行时用于记录数据
"""
变量（名称）= 一个值 一个可变的值



"""
# 定义一个变量
BianLiang1 = 20
# 再定义一个变量
BianLiang2 = 30
# 输出单个变量
print(BianLiang1)
print(BianLiang2)
# 输出多个变量
print(BianLiang1,BianLiang2)
# 变量与字符串混合输出
print("两个变量分别为：",BianLiang1,"和",BianLiang2)

# 变量值操作

# 赋值到原先变量
BianLiang2 = BianLiang2 - 10
print("变量2减10等于",BianLiang2)
BianLiang2 = BianLiang2 - 5
print("变量2再减5等于",BianLiang2)
print()
# 赋值到一个新的变量
BianLiang3 = BianLiang1 - 10
print("变量1减十等于",BianLiang3)

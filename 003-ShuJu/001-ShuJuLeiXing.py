# 查看数据类型：type()
# type()会输出一个返回值，可以被接受和操作
# 查看括号中的数据类型（字面量和变量）
# 与print一起使用，输出数据的类型
#直接输出字面量的数据类型
print(type(1))
print(type(1.5))
print(type("sssss111"))
# 输出变量中的值的数据类型
BianLian3 = "sssdsdq1sdasd"
print(type(BianLian3))
# 使用变量存储type
BianLian2 = type("11wwddaa")
BianLian1 = type(1)
print(BianLian1,BianLian2)

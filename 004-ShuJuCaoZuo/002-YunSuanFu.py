# 赋值运算符
# 1：= 将”=“右边的值赋予左边的变量
b_1 = 2**10+1024
print(b_1)
print("---------------------------------")
# 2:+= 将“=“左的变量本身应用与右边的运算，再将值赋回变量本身 即： "a += c"  ==  'a = a+c"
b_2 = 2
b_2 += 5
print(b_2)
print("---------------------------------")
# 3:-= 即："a += c"  ==  'a = a+c"
b_3 = 10
b_3 -= 2
print(b_3)
print("---------------------------------")
# 4: *= 即："a *= c"  ==  'a = a*c"
b_4 = 5
b_4 *= 2
print(b_4)
print("---------------------------------")
# 4: /= 即："a /= c"  ==  'a = a/c"
b_5 = 10
b_5 /= 2
print(b_5)
print("---------------------------------")
# 4: //= 即："a //= c"  ==  'a = a//c"
b_6 = 9
b_6 //=2
print(b_6)
print("---------------------------------")
# 4: %= 即："a %= c"  ==  'a = a%c"
b_7 = 9
b_7 %= 2
print(b_7)
print("---------------------------------")
# 4: **= 即："a **= c"  ==  'a = a**c"
b_8 = 2
b_8 **= 10
print(b_8)
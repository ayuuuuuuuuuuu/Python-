# 算数运算符
print("一加一=",1+1)
print("---------------------------------")
# 1: + 加
a_1 = 6
a_2 = 2
print(a_1+a_2)
print("---------------------------------")
# 2：- 减法
print(a_1-a_2)
print("---------------------------------")
# 3：* 乘
print(a_1*a_2)
print("---------------------------------")
# 4：/ 除
print(a_1/a_2)
print("---------------------------------")
# 5：// 取整除
print(a_1//a_2)
print("---------------------------------")
# 6：% 取余
print(a_1%a_2)
print("---------------------------------")
# 7：** 指数
print(a_1**a_2)
print("---------------------------------")
